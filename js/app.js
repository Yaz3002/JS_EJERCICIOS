/*función con parámetros:*/
function calcularAreaRectangulo(base, altura) {
  return  base * altura;
}

// Llamadas a la función con diferentes valores
console.log(calcularAreaRectangulo( 8, 3)); // Salida: 24
console.log(calcularAreaRectangulo( 15, 2)); // Salida: 30
console.log(calcularAreaRectangulo( 20, 5)); // Salida: 100